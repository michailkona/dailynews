package Model;

import java.sql.Date;

/**
 *
 * @author konstantina
 */
public class Article {
    
    private int article_id;
    private String article_title;
    private String article_text;
    private Date article_date_published;
    private int author_id;
    private int category_id;
    private String category_title;
    
    public int getArticle_id() {
        return article_id;
    }

    public void setArticle_id(int article_id) {
        this.article_id = article_id;
    }

    public String getArticle_title() {
        return article_title;
    }

    public void setArticle_title(String article_title) {
        this.article_title = article_title;
    }

    public String getArticle_text() {
        return article_text;
    }

    public void setArticle_text(String article_text) {
        this.article_text = article_text;
    }

    public Date getArticle_date_published() {
        return article_date_published;
    }

    public void setArticle_date_published(Date article_date_published) {
        this.article_date_published = article_date_published;
    }

    public int getAuthor_id() {
        return author_id;
    }

    public void setAuthor_id(int author_id) {
        this.author_id = author_id;
    }

    public int getCategory_id() {
        return category_id;
    }

    public void setCategory_id(int category_id) {
        this.category_id = category_id;
    }

    public String getCategory_title() {
        return category_title;
    }

    public void setCategory_title(String category_title) {
        this.category_title = category_title;
    }
    
    
    
}
